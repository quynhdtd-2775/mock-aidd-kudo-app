def file_description(name: str) -> str:
    """Return a one-line description for a filename, falling back to a prettified stem."""
    return _ARTIFACT_DESCRIPTIONS.get(name, name.replace("-", " ").replace(".md", ""))


# Per-subdir blurbs: one-line "what is this folder" shown at the top of each leaf README.
# Unknown subdirs fall back to empty string (safe — no crash, no blurb line emitted).
_SUBDIR_BLURBS: dict[str, str] = {
    "system": "Core system-level documentation: architecture, rules, and shared definitions.",
    "generated": "Auto-generated artifacts from rebuild-spec analysis passes.",
    "features": "Per-feature deep-dive specifications (business context, screens, technical spec, edge cases).",
    "screens": "Per-screen detailed UI and behavior specifications.",
    "flows": "Cross-cutting process flows that span more than one feature or service.",
    "components": "Per-component documentation for multi-component / system-of-systems projects.",
}


def subdir_blurb(rel: str) -> str:
    """Return a one-line blurb for a subdir (by its relative name), or '' if unknown."""
    # rel may be a plain name like "system" or a path like "system/subdir"; use the first segment.
    first_segment = rel.split("/")[0].split("\\")[0]
    return _SUBDIR_BLURBS.get(first_segment, "")


def read_user_tail(existing_content: str) -> str:
    """Extract user content below the end-generated marker (RT-F14 preservation)."""
    idx = existing_content.find(GEN_END)
    if idx == -1:
        return ""
    return existing_content[idx + len(GEN_END):]
